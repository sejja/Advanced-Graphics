CREATE TABLE CARPETA(

ID INTEGER PRIMARY KEY NOT NULL,

NOMBRE TEXT,

RUTA TEXT,

ID_CARPETA_SUPERIOR INTEGER,

FOREIGN KEY (ID_CARPETA_SUPERIOR) REFERENCES CARPETA(ID)

);



CREATE TABLE TIPO(

NOMBRE TEXT PRIMARY KEY NOT NULL,

RUTA_IMAGEN TEXT

);



CREATE TABLE ASSET(

ID INTEGER PRIMARY KEY AUTOINCREMENT,

NOMBRE TEXT,

RUTA TEXT,

TIPO TEXT,

CARPETA INTEGER,

CARGADO BOOLEAN,

FOREIGN KEY (CARPETA) REFERENCES CARPETA(ID)

FOREIGN KEY (TIPO) REFERENCES TIPO(NOMBRE)

);

